public class Q8 {
    public void promptGrade(char grade){
        if (grade == 'A') {
            System.out.println("You get "+ grade + " grade that means Excellent.");}
        if (grade == 'B') {
            System.out.println("You get "+ grade + " grade that means Good.");}
        if (grade == 'C') {
            System.out.println("You get "+ grade + " grade that means Average.");}
        if (grade == 'D') {
            System.out.println("You get "+ grade + " grade that means Deficient.");}
        if (grade == 'E') {
            System.out.println("You get "+ grade + " grade that means Failing.");}


    }

}
